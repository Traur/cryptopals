def gMul(a, b):
	p = 0x0
	for i in range(8):
		p ^= -(b & 1) & a
		m = -((a >> 7) & 0x1)
		a = (a << 1) ^ (0b100011011 & m)
		b >>= 1

	return p

def calcInverse():
	inverse = list([0])
	inverse = inverse*256
	for i in range(256):
		for j in range(256):
			tmp = gMul(i,j)
			if tmp == 0x1:
				inverse[i] = j

	return inverse

def rotateRightBinary(binary, n):
	n = n % 8
	for i in range(n):
		lsb = binary & 0x1
		shift = binary >> 1
		binary = (lsb << 7) | shift

	return binary

